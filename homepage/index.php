				<h2>PREPARING TO WRITE A RHYTHM</h2>

				<h3>(Pattern Programming)</h3>
				<ol>
					<li> Connect an amplifier or headphones to the output of your DR-110.</li>
					<li>	Turn the POWER on and set the VOLUME to a little under half.</li>

					<li>Turn TEMPO, BALANCE, and ACCENT to their mid-points. In the beginning, you may wish to set the TEMPO slower. The second line is a good place to start.</li>
					<li>Hold SHIFT button down and press button 4. STEP WRITE will appear in the lower right hand box of the LCD screen. This tells the DR-110 you are going to write a rhythm.</li>
					<li>Press BANK SELECTOR button (A|B|C|D) to A. A will appear under BANK (lower middle box of screen). If B appears, press button again for A.</li>
					<li>Press button 1. 1 will appear on the screen under RHYTHM. This tells the DR-110 you are going to store the beat in SELECTOR 1.</li>

					<li>Hold SHIFT button down and press 7 (under PATTERN CLEAR). This will erase anything previously stored in SELECTOR 1.</li>
					<li>Hold SHIFT button down and press 6 (12/16). You will see a little arrow at the top of the graph on your screen. The arrow will alternate between 12 and 16. Press 6 until the arrow is above 16. This tells the memory that you are writting a pattern with 4 beats in each measure and that each beat is divided in 4 parts for a total of 16. Since the procedure for preparing the DR. RHYTHM is always the same, the sooner you memorize this procedure, the more fun you'll have.</li>
				</ol>
				<h2>WRITING THE BEAT</h2>
				<p>Select the instrument that you are going to enter by pressing the corresponding button. For example, the Closed Hi-Hat. Press the C. HIHAT button and on the screen you will see a flashing black dot on the C. HIHAT line. (note: Dot flashes at tempo machine is set at)</p>
				<p>When you press the Start Button, a solid dot will appear. When you press the STOP key, the flashing dot will move to the next position. Each dot entered will indicate a note. </p>

				 <h2>WRITING A SONG</h2>
				 <ol>
					<li> Make sure that Rhythm is not playing.</li>
					<li> While holding the SHIFT Key down, press the button numbered 3. (Now the DR-110 is in the SONG WRITE mode.)</li>

					<li>Select either Song number I or II. (While holding the SHIFT Key down, press the BANK Selector Burron, and the number changes I and II alternately each time it is pressed)</li>
					<li>While holding the SHIFT Key down, press the button numbered 8. (Now the first measure of the Song is ready to start to play.)</li>
					<li>Press the START Key. (Rhythm will start playing)</li>
					<li>Select the Rhythm Pattern you wish to write into the first measure (The selected Rhythm Pattern is now shown in the Display and plays)</li>
					<li>Press the ENTER key (O HIHAT). (Now the selected Rhythm Pattern is written into the first measure, and it will move to the second measure)</li>
					<li>Select the Rhythm Pattern to be written into the second measure.</li>

					<li>Press the ENTER Key. (The selected Rhythm Pattern is written into the second measure and it moves to the third measure.)</li>
					<li>
						<ul>
						    <li> Likewise, keep writing up to the measures just before the final one.</li>
						    <li>If you are to write the same Rhythm Pattern as the one before, just pressing the ENTER key will be sufficient.</li>
						</ul>
					</li>

					<li>Select the Rhythm Pattern for the last measure.</li>
					<li>Press the D.C. (C. HIHAT) (Now D.C. [da capo] is set to the final measure.)</li>
					<li>Press the ENTER key. (The selected Rhythm Pattern is now written into the final measure, and it moves to the next measure.)</li>
					<li>Press the STOP Key. Now try playing the Rhythm. </li>
				</ol>
