				<ul>
					<li><em>Release Date</em>: Sometime in 1983. There were several revisions of the manual - The back of the first manual I had said "DR-110 OWNER'S MANUAL '84 SEP. A-4" and I have another that's marked "'83 NOV. B-4". <a href="Boss-dr110-manual.pdf">A PDF version of the Boss DR-110 manual</a> is labeled "'86 MAR. C-3"</li>

					<li><em>Number Manufactured</em>: Well, at least 5000, going by my serial number.</li>
					<li><em>Synthesis Type</em>: 100% Pure Analogue</li>
					<li><em>Original 1983 Price</em>: $200</li>
					<li><em>Fair Second Hand Price</em>: $100-$200</li>
					<li><em>Included with original purchase</em>: Silver vinyl carrying case, 2 silver boss stickers, 55 page manual</li>

					<li><em>Optional Adapters</em>: Boss PSA-120, 220 or 240</li>
					<li>At the time, Roland produced a 40 page booklet (written by Sandy Feldstein) for the DR-110, "A Step-By-Step Approach to learning the Dr. Rhythm Graphic" (As if the manual wasn't simple enough). "This Handy Guide will teach you how to:
					<ul>
					        <li>Write Rock Beats</li>

					        <li>Write Rock Fills</li>
					        <li>Write a Rhythm Chart for a 12-Bar Blues</li>
					        <li>Write a Rhythm Chart for a 32-Bar Tune</li>
					        <li>Create Your Own Rhythms and Charts"</li>
					</ul>
				    It is pretty cheesy, and was sold for $2.95 retail by Alfred Publishing Co., Inc. (Book #2453) This was also published in 1984.</li>

				  <li><em>Test Mode</em>: When you turn on your DR-110, hold down the start and stop buttons. The entire LED screen should show, though dimly. Press all your blue buttons, and if they all work, a big OK should appear.</li>
